package com.como.como_nest;

public class Boats {
    private String namee;
    private int address;

    public Boats(String namee, int address) {
        this.namee = namee;
        this.address = address;
    }


    public String getNamee() {
        return namee;
    }

    public int getAddress() {
        return address;
    }
}
